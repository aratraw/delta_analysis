// ----------------------------------------------------------------------------
// 4.1. СОВПАДЕНИЕ DEC-ЛАПЛАСИАНА С КОТАНГЕНСНЫМ
// ----------------------------------------------------------------------------
//
// НЕ ТЕСТИРУЕТСЯ. Тест HodgeLaplacianMatchesCotangent удалён.
//
// ПРИЧИНА:
//   Равенство (δdf)(v) = (L_cot f)(v) / |*v| выполняется ТОЛЬКО для
//   circumcentric dual (дуала Вороного). Наш DualComplex строит
//   БАРИЦЕНТРИЧЕСКИЙ дуал, в котором отношение |⋆e|/|e| не равно
//   котангенсам. Следовательно, DEC-лапласиан математически не обязан
//   совпадать с котангенсным, и тест был некорректен.
//
//   При необходимости совпадения в будущем следует реализовать
//   CircumcentricDualComplex и переключить DiscreteForm на него.
//
// ССЫЛКА: см. раздел 4 сопроводительного комментария в discrete_forms.h

// ----------------------------------------------------------------------------
// 4.2. ГЛОБАЛЬНАЯ САМОСОПРЯЖЁННОСТЬ НА СЕТКАХ С ГРАНИЦЕЙ
// ----------------------------------------------------------------------------
//
// НЕ ТЕСТИРУЕТСЯ. Попытка проверки ⟨δdf, g⟩_⋆ = ⟨f, δdg⟩_⋆ провалилась.
//
// ПРИЧИНА:
//   На многообразиях с краем ⟨δdf, g⟩ = ⟨df, dg⟩ − ∮_∂ g ⋆df, и граничный
//   член не сокращается. Поэтому глобальная симметрия не обязана выполняться.
//   Для замкнутой сетки свойство было бы верным, но мы работаем с сетками,
//   имеющими границу.
//
//   Вместо этого проверяем точечную симметрию на внутренних вершинах, где
//   граничный вклад отсутствует.

