// ----------------------------------------------------------------------------
// 3.1. Внешняя производная d
// ----------------------------------------------------------------------------
//
// Тесты:
//   DerivativeOf0FormOnTriangle
//   DerivativeOf0FormOnSquare
//   DSquareZeroFor0Form
//   DOf1FormGives2Form
//   DSquareZeroOnTetrahedron
//
// ЧТО ПРОВЕРЯЕТСЯ:
//   - d на 0-формах даёт разность значений на вершинах ребра:
//     (df)(e) = f(v1) - f(v0). Это ТОЧНОЕ определение.
//   - d на 1-формах создаёт 2-форму (сумма по границе с учётом знаков).
//   - d² = 0 ТОЧНО для 0-форм на треугольниках и тетраэдрах.
//
// МЕТОДОЛОГИЯ:
//   - Задаём конкретные значения в вершинах и сравниваем df с ожидаемым.
//   - Для d²=0: заполняем форму случайными значениями, вычисляем ddf и
//     проверяем, что все элементы равны 0 (точное равенство).
//
// ПОЧЕМУ ИМЕННО ЭТИ ТЕСТЫ:
//   - Первые два теста проверяют, что d реализован через incident_faces
//     с правильными знаками.
//   - d²=0 — ФУНДАМЕНТАЛЬНЫЙ инвариант, не зависящий от метрики или дуала.
//     Если он нарушен, всё остальное не имеет смысла.

// ----------------------------------------------------------------------------
// 3.2. Звезда Ходжа ⋆
// ----------------------------------------------------------------------------
//
// Тест: HodgeStarOnTriangle
//
// ЧТО ПРОВЕРЯЕТСЯ:
//   - ⋆ от 0-формы (константа 1) даёт 2-форму, интеграл которой равен
//     интегралу исходной функции: Σ (⋆f)(τ)·|τ| = Σ f(v)·|*v| = mesh_area.
//   - Для константной f=1 это означает, что сумма ⋆f(τ)·|τ| = площадь сетки.
//
// МЕТОДОЛОГИЯ:
//   - Вычисляем star_f, затем интегрируем: Σ star_f[t] * simplex_volume(2,t).
//   - Сравниваем с площадью сетки (1/2 для единичного треугольника).
//
// ПОЧЕМУ ИМЕННО ЭТОТ ТЕСТ:
//   - Интегральное свойство ⋆ является определяющим и не зависит от типа дуала.
//   - Предыдущая версия теста (сумма значений ⋆f без умножения на площадь)
//     была НЕВЕРНА и маскировала ошибку в реализации (лишний множитель vol).
//     Исправленный тест гарантирует корректность ⋆ для 0-форм.

// ----------------------------------------------------------------------------
// 3.3. DEC-лапласиан и кодифференциал
// ----------------------------------------------------------------------------
//
// Тесты:
//   HodgeLaplacianConsistency
//   CodifferentialOf1FormOnTriangle
//   LaplaceOn1Form
//
// ЧТО ПРОВЕРЯЕТСЯ:
//   - Лапласиан от константы (δdf для f≡1) равен 0 во ВСЕХ вершинах.
//   - Лапласиан линейной функции f(x,y)=x равен 0 на ВНУТРЕННЕЙ вершине
//     (свойство, специфичное для симметричной сетки с барицентрическим дуалом).
//   - Кодифференциал 1-формы возвращает 0-форму правильного размера.
//   - Лапласиан 1-формы возвращает 1-форму правильного размера.
//
// МЕТОДОЛОГИЯ:
//   - Для Consistency: создаём константную и линейную формы, вычисляем δd,
//     проверяем на нуль.
//   - Для CodifferentialOf1Form/LaplaceOn1Form: проверяем только размер
//     результата, так как конкретные значения зависят от геометрии.
//
// ПОЧЕМУ ИМЕННО ЭТИ ТЕСТЫ:
//   - Константа в ядре — универсальное свойство ЛЮБОГО корректного лапласиана.
//   - Нуль на линейной функции для симметричной внутренней вершины —
//     дополнительная проверка правильности нормировок в star и codifferential.
//   - Размеры результатов гарантируют, что цепочка операций не нарушает
//     структуру форм.
//
// ИСТОРИЧЕСКИЕ ПРОБЛЕМЫ (см. раздел 5):
//   - Тест HodgeLaplacianMatchesCotangent был УДАЛЁН, так как требовал
//     совпадения DEC-лапласиана с котангенсным, что верно только для
//     circumcentric dual. Для барицентрического дуала это не выполняется.
//   - Тест на глобальную симметрию ⟨δdf, g⟩ = ⟨f, δdg⟩ провалился из-за
//     граничных членов и был заменён на точечную проверку на внутренней вершине.

// ----------------------------------------------------------------------------
// 3.4. Клиновое произведение ∧
// ----------------------------------------------------------------------------
//
// Тест: WedgeProductOf1Forms
//
// ЧТО ПРОВЕРЯЕТСЯ:
//   - АНТИСИММЕТРИЯ: α ∧ α = 0 для любой 1-формы α.
//
// МЕТОДОЛОГИЯ:
//   - Задаём конкретные значения на рёбрах, вычисляем a∧a, проверяем нуль.
//
// ПОЧЕМУ ИМЕННО ЭТОТ ТЕСТ:
//   - Антисимметрия — определяющее свойство внешнего произведения.
//   - Проверка на конкретных числах выявляет ошибки в формуле и знаках.

// ----------------------------------------------------------------------------
// 3.5. Граничные условия
// ----------------------------------------------------------------------------
//
// Тест: DirichletBoundaryOn0Form
//
// ЧТО ПРОВЕРЯЕТСЯ:
//   - Можно установить значения на граничных вершинах и внутренней вершине
//     независимо, и они сохраняются.
//
// МЕТОДОЛОГИЯ:
//   - Создаём сетку с центром, задаём граничные значения, случайное внутреннее,
//     проверяем, что они не изменились.
//
// ПОЧЕМУ ИМЕННО ЭТОТ ТЕСТ:
//   - Базовый тест целостности данных для последующего использования в
//     решателях с граничными условиями Дирихле.

