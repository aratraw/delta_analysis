        ASSERT_NE(e20, -1);

        a[e01] = 1_r; a[e12] = 2_r; a[e20] = 3_r;
        b[e01] = 4_r; b[e12] = 5_r; b[e20] = 6_r;

        auto a_wedge_a = wedge(a, a);
        for (std::size_t t = 0; t < a_wedge_a.size(); ++t)
            EXPECT_EQ(a_wedge_a[t], 0_r);
